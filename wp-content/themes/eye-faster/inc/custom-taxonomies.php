<?php


function service_category_taxonomy() {  
    register_taxonomy(  
        'service_category',  //The name of the taxonomy. Name should be in slug form (must not contain capital letters or spaces). 
        'services',        //post type name
        array(  
            'hierarchical' => true,  
            'labels' => array(
				'name' => _x( 'Service Category', 'sfhr' ),
				'singular_name' => _x( 'Service Category', 'sfhr' ),
				'search_items' =>  __( 'Search Service Categories', 'sfhr' ),
				'all_items' => __( 'All Service Categories', 'sfhr' ),
				'parent_item' => __( 'Parent Service Category', 'sfhr' ),
				'parent_item_colon' => __( 'Parent Service Category:', 'sfhr' ),
				'edit_item' => __( 'Edit Service Category', 'sfhr' ),
				'update_item' => __( 'Update Service Category', 'sfhr' ),
				'add_new_item' => __( 'Add New Service Category', 'sfhr' ),
				'new_item_name' => __( 'New Service Category Name', 'sfhr' ),
				'menu_name' => __( 'Service Categories', 'sfhr' ),
			),
            'query_var' => true,
            'rewrite' => array(
                'slug' => 'service_category', // This controls the base slug that will display before each term
                'with_front' => false // Don't display the category base before 
            )
        )  
    );  
}  
add_action( 'init', 'service_category_taxonomy');



?>